    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.socio.setAntiguedad(this.jXDatePicker1.getDate());
        this.socio.setDireccion(this.jTextArea1.getText());
        this.socio.setDni(this.jTextField2.getText());
        this.socio.setNombre(this.jTextField1.getText());
        this.socio.setTelefono(Integer.parseInt(this.jTextField3.getText()));
        this.socio.setPenyas((Penyas)this.jTextObject1.getSelectObject());
        for(int i = 0; i<this.rs.size(); i++){
            InterpenyasRubielos.rsm.saveDato(new RopaSocios(rs.get(i),this.socio));
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

